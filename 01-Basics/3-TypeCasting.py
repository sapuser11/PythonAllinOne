# Type Casting in Python - str(), int(), float(), bool() 

age = 25

#Conver age to string

age = str(age)
print("Age", age, "is type of", type(age))

#Conver age to float

age = float(age)
print("Age", age, "is type of", type(age))

#Conver age to int

age = int(age)
print("Age", age, "is type of", type(age))

#Conver age to bool

age = bool(age)
print("Age", age, "is type of", type(age))
