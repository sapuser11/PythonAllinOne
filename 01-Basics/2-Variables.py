# Scalar Variables - Store a single value

# Integer
age = 25
print("Age", age, "is type of", type(age))
# Float
height = 5.9
print("Height", height, "is type of", type(height))

# String
name = "Devendra Singh"
print("Name", name, "is type of", type(name))

# Boolean
isStudent = True
print("isStudent", isStudent, "is type of", type(isStudent))

# Character
grade = 'A'
print("Grade", grade, "is type of", type(grade))

