# List DataType
list = [1, 2, 3, 4, 5, 8, 7]

print("Print List : ", list)

list.append(6)
print("Append List : ", list) 

list.insert(0, 0)
print("Insert List : ", list) 

list.insert(7, 10)
print("Insert List : ", list) 

list.remove(10)
print("Remove List : ", list) 

list.pop()
print("Pop List : ", list) 

list.sort()
print("Sort List : ", list) 

list.reverse()
print("Reverse List : ", list) 

list.copy()
print("Copy List : ", list) 

list.clear()
print("Clear List : ", list)
