# Example of Tuples

tuples = (1, 2, 3, 4, 5, 6, 7, 8)
print("Print Tuples : ", tuples)

# Index of tuples
print("Index of Tuples : ", tuples[0])

# Slicing of tuples
print("Slicing of Tuples : ", tuples[0:3])

# Length of tuples
print("Length of Tuples : ", len(tuples))

# Count of tuples
print("Count of Tuples : ", tuples.count(1))

# Index of tuples
print("Index of Tuples : ", tuples.index(1))

# Reverse of tuples
print("Reverse of Tuples : ", tuples[::-1])

# Sort of tuples
print("Sort of Tuples : ", sorted(tuples))

# Min of tuples
print("Min of Tuples : ", min(tuples))

# Max of tuples
print("Max of Tuples : ", max(tuples))

# Sum of tuples
print("Sum of Tuples : ", sum(tuples))

# Average of tuples
print("Average of Tuples : ", sum(tuples) / len(tuples))

